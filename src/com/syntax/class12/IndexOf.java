package com.syntax.class12;

public class IndexOf {
    public static void main(String[] args) {
        String str = "I am always confused";
        System.out.println(str.indexOf("a")); //index no: 2
        System.out.println(str.indexOf("s"));//index no: 10
        System.out.println(str.indexOf(" "));//index no: 1
    }
}
