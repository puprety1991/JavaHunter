package com.syntax.class02;

public class ArithmeticOperators {
	public static void main(String[] args) {
		
		
		int a =800;
		int b=200;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println();
		
		int c=10;
		int d=2;
		int sum, sub, mult, div, rem;
		sum = c+d;
		sub = c-d;
		mult= c*d;
		div = c/d;
		rem = c%d;
		
		
		System.out.println("addition of two number is "+sum);
		System.out.println("substraction of two number is "+sub);
		System.out.println("multiplication of two number is "+mult);
		System.out.println("division of two number is "+div);
		System.out.println("remainder of two number is "+rem);
		System.out.println();
		
		float num1 = 10.0f;
		float num2 = 5.54f;
		System.out.println("Division of float = "+num1/num2);
		System.out.println();
		double nums1 = 323.43;
		double nums2 = 2.33;
		System.out.println("Division of double = "+nums1/nums2);
				
		
		
	}

}
