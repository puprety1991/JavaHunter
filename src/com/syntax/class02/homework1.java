package com.syntax.class02;

public class homework1 {
	public static void main(String[] args) {
		
		/*
		 * 
		 * Write a Java program to add, subtract, multiply and divide 2 decimal values. 
		 * Your program should say. “The __ of 2 numbers __ and  is equal to __”
		 */
		
		int num1, num2;
		num1 = 20;
		num2 = 10;
		
		System.out.println("The sum of 2 numbers "+ num1+", "+num2+" and is equal to "+(num1+num2));
		System.out.println("The subtraction of 2 numbers "+ num1+", "+num2+" and is equal to "+(num1-num2));
		System.out.println("The multiplication of 2 numbers "+ num1+", "+num2+" and is equal to "+(num1*num2));
		System.out.println("The division of 2 numbers "+ num1+", "+num2+" and is equal to "+(num1/num2));

				
	}

}
