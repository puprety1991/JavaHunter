package com.syntax.class07;

public class ShorthandOperators {
	public static void main(String[] args) {
		 int i =10;
		 i = i+9;
		 System.out.println(i);
		 
		 i = i-5;
		 System.out.println(i);
		 
		 i = i*2;
		 System.out.println(i);
		 System.out.println("----------------");
		 
		 int a = 10;
		 
		 a+=9;
		 System.out.println(a);
		 
		 a-=5;
		 System.out.println(a);
		 
		 a*=2;
		 System.out.println(a);
	}

}
