package com.syntax.class03;

public class Ifelse5 {
	public static void main(String[] args) {
		
		int money = 65001;
		
		if(money >= 65000) {
			System.out.println("I can buy a Tesla");
		}else {
			System.out.println("I need to save more");
		}
		
		if(money == 65000) {
			System.out.println("I can buy a Tesla");
		}else {
			System.out.println("I need to save more");
		}
		
	}

}
