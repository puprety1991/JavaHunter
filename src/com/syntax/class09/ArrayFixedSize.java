package com.syntax.class09;

public class ArrayFixedSize {
	public static void main(String[] args) {
		int[] nums = new int[3];
		nums[0] = 40;
		nums[1] = 54;
		nums[2] = 45;
		System.out.println(nums[0]);
		String[] colors = new String[3];
		colors[0] = "White";
		colors[1] = "Pink";
		colors[2] = "Black";
		System.out.println(colors[0]);
	}

}
